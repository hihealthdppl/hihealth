			<div id="blog">
				<h3>5 Penyakit Ini Bisa Dideteksi Lewat Kondisi Tangan</h3>
				<small>Posted October 27, 2020 by <a href="https://www.liputan6.com/global/read/4393363/5-penyakit-ini-bisa-dideteksi-lewat-kondisi-tangan">Teddy Tri S.B (Liputan6.com)</a></small><img src="images/tangan.jpg" alt="">
				<p>
					Sumber Utama :<a href = "https://www.liputan6.com/global/read/4393363/5-penyakit-ini-bisa-dideteksi-lewat-kondisi-tangan">Teddy Tri S.B (Liputan6.com)</a>
				</p>
				<p>
					Liputan6.com, Jakarta - Rupanya, tangan adalah bagian tubuh yang dapat menunjukkan kondisi seseorang, apakah sehat atau sakit. Meski terlihat sederhana, tangan dapat merefleksikan kesehatan manusia.
				</p>
				<p>
					Salah satu cara mengidentifikasi penyakit adalah dengan membedakan perubahan warna, gerakan hingga kondisi fisik tangan.
				</p>
				<p>
					Beda warna, kondisi dan gerakan maka akan beda pula jenis gejala yang dialami oleh seseorang. Dengan cara seperti ini Anda bisa mencegah datangnya penyakit lebih cepat dari sebelumnya.
				</p>
				<p>
					Seperti dikutip dari laman Health.com, Selasa (27/10/2020), berikut 5 penyakit yang bisa diketahui lewat kondisi tangan:
				</p>
				<p>
					<h4>1. Genggaman Tangan Melemah</h4>
				</p>
				<p>
					Dalam dunia bisnis, jabatan tangan yang lemah dikaitkan dengan kepribadian seseorang. Namun, dalam dunia kedokteran, ini bisa menjadi masalah kesehatan Anda.
				</p>
				<p>
					"Selama pemeriksaan fisik untuk pasien, kami benar-benar memerhatikan genggaman pasien," ujar Anne Albers, seorang ahli jantung.
				</p>
				<p>
					Menurut ulasan pada 2016 di International Journal of Cardiology, kekuatan cengkeraman yang menurun ditambah dengan gaya berjalan lambat, menandakan adanya risiko kematian yang lebih tinggi dari penyakit jantung.
				</p>
				<p>
					<h4>2. Benjolan Merah Serta Lecet</h4>
				</p>
				<p>
					Ruam merah di tangan atau pergelangan yang seketika melepuh atau berdarah diindikasikan bahwa Anda mengalami alergi nikel.
				</p>
				<p>
					Kepekaan terhadap nikel adalah salah satu penyebab paling umum dermatitis kontak alergi, menurut American Academy of Dermatology.
				</p>
				<p>
					Banyak benda yang menyentuh kulit Anda mengandung nikel: gelang, jam tangan, cincin, bahkan telepon seluler.
				</p>
				<p>
					<h4>3. Perubahan Warna Jari</h4>
				</p>
				<p>
					Jika jari Anda pucat (berarti Anda kekurangan darah) dan apabila biru (atau ungu atau hitam) ketika dingin atau Anda sedang stres.
				</p>
				<p>
					Perubahan suhu atau keadaan emosi yang tiba-tiba dapat memicu kondisi ini, yang menyebabkan hilangnya aliran darah ke jari atau jari kaki secara sementara.
				</p>
				<p>
					<h4>4. Tremor Tangan</h4>
				</p>
				<p>
					Terkadang guncangan tangan bukan masalah besar, dan di lain waktu itu bisa menjadi tanda penyakit neurologis.
				</p>
				<p>
					Setiap orang memiliki sedikit guncangan di tangan mereka. Ini disebut tremor fisiologis, Dr. Barrett menjelaskan, dan ketika Anda kurang tidur, terlalu banyak minum kopi, mengonsumsi obat-obatan tertentu, hingga konsumsi alkohol berlebih akan menyebabkan itu terjadi.
				</p>
				<p>
					<h4>5. Nyeri Tangan, Kaku dan Bengkak</h4>
				</p>
				<p>
					Seseorang yang mengalami nyeri, kaku dan bengkak pada tangan dapat diindentifikasikan sebagai ciri sejumlah penyakit.
				</p>
				<p>
					Sejumlah penyakit itu adalah rheumatoid arthritis, psoriatic arthritis, lupus, vasculitis (radang pembuluh darah), scleroderma (penyakit jaringan ikat), dan dermatomiositis (penyakit kulit dan otot).
				</p>
				<p>
					Tangan bengkak juga menjadi ciri-ciri bahwa seseorang mengalami radang usus," kata Dr. Weselman.
				</p>
				<p>
					"Biasanya akan juga ditemukan di bagian lutut dan pergelangan kaki," katanya.
				</p>
