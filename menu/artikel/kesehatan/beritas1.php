				<h3>Bebas Stres dengan 1 Menit Yoga Tertawa</h3>
				<small>Posted November 11, 2020 by <a href="https://www.cnnindonesia.com/gaya-hidup/20201111090009-255-568372/bebas-stres-dengan-1-menit-yoga-tertawa">CNN Indonesia</a></small><img src="images/yoga2.jpeg" alt="">
				<p>
					Sumber Utama :<a href = "https://www.cnnindonesia.com/gaya-hidup/20201111090009-255-568372/bebas-stres-dengan-1-menit-yoga-tertawa">CNN Indonesia</a>
				</p>
				<p>
					Jakarta, CNN Indonesia -- Yoga identik dengan aneka gerakan tubuh yang fleksibel dan sulit dilakukan. Namun pada kenyataannya, yoga tak melulu harus diikuti dengan aktivitas fisik seperti itu. Salah satu yang paling mudah dilakukan adalah yoga tertawa. Seperti namanya, yang harus Anda lakukan hanyalah tertawa.
				</p>
				<p>
					"Yoga tertawa kalo dilihat dari kitab yoga sutra patanjali adalah proses pembersihan dari tubuh kita melalui gelombang pikiran yang kita getarkan lewat tertawa kita melepas beban pikiran, melepas penat kita sehingga kita jadi plong. Dan ini akan menyehatkan kita. Tertawa bagian dari asana," kata Eka Sukma Putra, Founder Eka Sukma Yoga Foundation kepada CNNIndonesia.com.
				</p>
				<p>
					Hanya saja Eka menyebut bahwa tertawa untuk yoga ini berbeda dengan tertawa sehari-hari. Dalam yoga tertawa ini, tertawa dilakukan dengan kesadaran penuh dan tak perlu alasan atau hal untuk untuk mulai tertawa.
				</p>
				<p>
					"Caranya? Tertawa dalam yoga itu harus lepaskan dulu dari obyek yang di luar diri. Kemudian lakukan getaran tertawa dari dalam pusatkan dengan diri sehingga kita selaras dan harmonis dalam tubuh yang menyeimbangkan semua hormon endokrin dan menyeimbangkan homeostasis dan saraf-saraf otonom," ucapnya.
				</p>
				<p>
					"Tertawa itu getarannya melepaskan, membersihkan, senyum muncul dari kedamaian dari kebersihan jiwa senyum dari dalam, inner beauty dan tertawa outer beauty. Senyum itu rangkaian dari tertawa."
				</p>
				<p>
					Idealnya, yoga tertawa ini dilakukan bersama dengan latihan napas, aktivitas tubuh, dan dilanjutkan dengan meditasi. Waktu yang dibutuhkan pun bervariasi antara 10-30 menit. Hanya saja, Anda juga bisa yoga tertawa dalam waktu singkat tanpa diikuti dengan meditasi.
				</p>
				<p>
					Untuk menghilangkan stres, yoga tertawa ini bisa dilakukan setiap saat. Termasuk saat Anda tengah merasa jenuh dan penat saat bekerja di depan laptop. Anda hanya perlu menyisihkan waktu setidaknya satu menit untuk tertawa.
				</p>
				<p>
					"Buka jendela lalu memandang yang jauh. Setelah itu pejamkan mata dan mulailah tertawa. Sadari tawa itu dan rasakan getaran tertawa di seluruh tubuh Anda," kata pria yang juga menjadi resident yoga guru di Amankila Resort, Manggis, Bali ini.
				</p>
				<p>
					"Yang penting pusatkan pikiran, mindful agar manfaatnya lebih terasa. Tanpa mindful, tertawa hanya akan sementara saja. Misalnya saat Anda nonton komedi atau bercanda dengan kawan, itu sementara saja."
				</p>
				<p>
					Ada berbagai manfaat yang didapat dari yoga tertawa, misalnya untuk menjaga kesehatan tubuh, mendamaikan diri, menghilangkan stres serta depresi, sampai menjaga keseimbangan emosi.
				</p>
				<p>
					<h4>Sejarah Yoga Tertawa</h4>
				</p>
				<p>
					Yoga Tertawa dipopulerkan oleh dokter keluarga Madan Kataria yang memodernisasi dan menyederhanakan yoga tertawa di masa awalnya. Mengutip berbagai sumber, Sesi yoga tawa dapat dimulai dengan teknik pemanasan lembut yang meliputi peregangan, nyanyian, tepuk tangan, kontak mata dan gerakan tubuh, untuk membantu memecah hambatan dan mendorong rasa bermain. Latihan pernapasan digunakan untuk mempersiapkan paru-paru untuk tertawa, diikuti dengan serangkaian 'latihan tertawa' yang menggabungkan metode akting dan teknik visualisasi dengan main-main.
				</p>
				<p>
					Latihan tertawa diselingi dengan latihan pernapasan. Tertawa selama dua puluh menit sudah cukup untuk mengembangkan manfaat fisiologis sepenuhnya.
				</p>
				<p>
					"Ini baik dilakukan saat ini, karena di masa sekarang ini orang kurang banyak tertawa, tapi stres. Maka tertawa saja, ini bagian dari proses penerimaan diri, bukan penolakan. Semakin banyak menolak hal-hal yang terjadi saat ini, maka semakin stres."
				</p>
