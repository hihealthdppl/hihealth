				<h3>Jangan Remehkan, Waspada 12 Tanda Gejala Umum Ini Mungkin Pertanda Sakit Parah</h3>
				<small>Posted November 17, 2020 by <a href="https://www.liputan6.com/global/read/4410534/jangan-remehkan-waspada-12-tanda-gejala-umum-ini-mungkin-pertanda-sakit-parah">Liputan6.com</a></small><img src="images/waspadaa.jpeg" alt="">
				<p>
					Sumber Utama :<a href = "https://www.liputan6.com/global/read/4410534/jangan-remehkan-waspada-12-tanda-gejala-umum-ini-mungkin-pertanda-sakit-parah">Liputan6.com</a>
				</p>
				<p>
					Liputan6.com, Jakarta - Kapan ketika Anda batuk hanya dianggap sekadar batuk saja, atau merasa sakit kepala merupakan tanda gejala yang harus dikhawatirkan?
				</p>
				<p>
					Anda harus waspada, hal itu bisa saja sebuah pertanda dan gejala penyakit yang dapat mengidentifikasikan kondisi kesehatan Anda serius. Ketika terjadi dan menghkhawatirkan, maka jangan ragu untuk menemui dokter.
				</p>
				<p>
					Seperti dilansir dari Medicinenet, Selasa (17/11/2020), terkadang gejala di satu bagian tubuh merupakan adanya tanda permasalahan pada bagian tubuh yang lainnya. Selain itu juga gejala penyakit yang tidak terkait dan terlihat biasa saja, kemungkinan merupakan tanda pertanda penyakit atau kondisi medis yang lebih serius.
				</p>
				<p>
					Maka dari itu Anda harus memperhatikan gejala kecil pada tubuh dengan rinci, lalu sampaikan ke dokter Anda.
				</p>
				<p>
					Berikut ini 12 gejala penyakit yang umum terjadi pada manusia, bisa saja pertanda penyakit parah:
				</p>
				<p>
					<h4>Sakit Jantung Hingga Masalah Pada Payudara</h4>
				</p>
				<p>
					<h5>1. Tanda Serangan Jantung</h5>
				</p>
				<p>
					Adanya rasa nyeri, rasa sebuah tekanan seperti rasa teremas atau sesak yang berlangsung beberapa menit, ketidaknyamanan di area lain tubuh bagian atas, keringat dingin, mual atau pusing.
				</p>
				<p>
					<h5>2. Tanda-Tanda Stroke </h5>
				</p>
				<p>
					Wajah terkulai, lengan terasa lemas, kesulitan dalam berbicara, pusing atau keseimbangan yang berkembang pesat, mati rasa atau tiba-tiba mengalami kelemahan secara medadak, kehilangan penglihatan, kebingungan, atau sakit kepala yang parah.
				</p>
				<p>
					<h5>3.  Gejala Masalah Kesehatan Dalam Reproduksi</h5>
				</p>
				<p>
					Adanya pendarahan atau bercak di antara periode dengan rasa gatal, terbakar, atau iritasi pada area genital yang merupakan adanya rasa ketidaknyamanan saat berhubungan seks, pendarahan menstruasi yang berat atau mengakibatkan nyeri panggul/ perut yang parah, keputihan yang tidak biasa, adanya rasa keram di perut bagian bawah dan sering buang air kecil atau urgensi kencing.
				</p>
				<p>
					<h5>4. Gejala Masalah Pada Payudara </h5>
				</p>
				<p>
					Seperti keluarnya cairan dari puting, nyeri yang tidak biasa pada payudara atau puting, adanya benjolan atau penebalan di dalam atau di dekat payudara atau di bagian area ketiak.
				</p>
				<p>
					<h4>Masalah Paru-Paru Hingga Kulit</h4>
				</p>
				<p>
					<h5>5. Gejala Masalah Paru-Paru </h5>
				</p>
				<p>
					Batuk berdarah, sesak napas, kesulitan bernapas, batuk kronis, serangan bronkitis ataupun pneumonia berulang dan mengi.
				</p>
				<p>
					<h5>6. Gejala Masalah Perut atau Pencernaan</h5>
				</p>
				<p>
					Pendarahan rektal, adanya darah di tinja atau tinja berwarna hitam, perubahan kebiasaan buang air besar atau tidak bisa mengontrol buang air besar, sembelit, diare, mulas atau asam lambung, dan juga muntah darah.
				</p>
				<p>
					<h5>7. Gejala Masalah Kandung Kemih</h5>
				</p>
				<p>
					Sulit atau nyeri saat buang air kecil, sering buang air kecil, kehilangan kontrol kandung kemih, adanya darah dalam urin, sering terbangun di malam hari untuk buang air kecil, mengompol pada malam hari atau kencing bocor.
				</p>
				<p>
					<h5>8. Gejala Masalah Kulit</h5>
				</p>
				<p>
				Perubahan tahi lalat di kulit, wajah dan leher sering kemerahan, penyakit kuning, lesi kulit yang tidak hilang atau sembuh, adanya pertumbuhan tahi lalat yang baru pada kulit, kulit tebal berwarna merah dengan bercak keperakan.
				</p>
				<p>
					<h4>Masalah Otot Hingga Berat Badan</h4>
				</p>
				<p>
					<h5>9. Gejala Masalah Otot Atau Persendian</h5>
				</p>
				<p>
					Nyeri otot yang terus menerus, nyeri pada tubuh seperti mati rasa atau kesemutan, kaku, bengkak, radang atau kemerahan di sekitar persendian dan penurunan rentang gerak atau hilangnya sendi atau otot.
				</p>
				<p>
					<h5>10. Gejala Masalah Emosional</h5>
				</p>
				<p>
					Adanya rasa kecemasan, depresi kelelahan, perasaan tegang, mimpi buruk, tidak tertarik pada aktivitas rutin, pikiran untuk bunuh diri, halusinasi dan delusi.
				</p>
				<p>
					<h5>11. Gejala Masalah Sakit Kepala</h5>
				</p>
				<p>
					Sakit kepala secara tiba-tiba, sakit kepala terburuk dalam hidup Anda, sakit kepala yang berhubungan dengan pusing parah seperti mual, muntah dan ketidakmampuan untuk berjalan.
				</p>
				<p>
					<h5>12. Gejala Masalah Makan Atau Berat Badan</h5>
				</p>
				<p>
					Rasa haus yang ekstrem, dehidrasi, rasa lapar yang berlebihan, kehilangan berat badan tanpa berusaha, makan berlebihan, muntah, kelaparan, kesibukan dengan makanan dan berat badan, olahraga kompulsif, penyalahgunaan obat pencahar atau pil diet dan depresi.
				</p>
				<p>
					Reporter : Romanauli Debora
				</p>
